package att;

public class Cadeira {
	String acento;
	String bracos;
	String material;
	String rodas;
	
	String inclina;
	String gira;
	String anda;
	String sobeedesce;
	
	public String getAcento() {
		return acento;
	}
	public void setAcento(String acento) {
		this.acento = acento;
	}
	public String getBracos() {
		return bracos;
	}
	public void setBracos(String bracos) {
		this.bracos = bracos;
	}
	public String getMaterial() {
		return material;
	}
	public void setMaterial(String material) {
		this.material = material;
	}
	public String getRodas() {
		return rodas;
	}
	public void setRodas(String rodas) {
		this.rodas = rodas;
	}
	public String getInclina() {
		return inclina;
	}
	public void setInclina(String inclina) {
		this.inclina = inclina;
	}
	public String getGira() {
		return gira;
	}
	public void setGira(String gira) {
		this.gira = gira;
	}
	public String getAnda() {
		return anda;
	}
	public void setAnda(String anda) {
		this.anda = anda;
	}
	public String getSobeedesce() {
		return sobeedesce;
	}
	public void setSobeedesce(String sobeedesce) {
		this.sobeedesce = sobeedesce;
	}

}
