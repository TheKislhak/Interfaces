package att;

public class Usu {
	int id;
	String nome;
	int password;
	String permissao;
	String usuario;

	String acesso;
	String logar;
	String deslogar;
	String excluiconta;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getPassword() {
		return password;
	}
	public void setPassword(int password) {
		this.password = password;
	}
	public String getPermissao() {
		return permissao;
	}
	public void setPermissao(String permissao) {
		this.permissao = permissao;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getAcesso() {
		return acesso;
	}
	public void setAcesso(String acesso) {
		this.acesso = acesso;
	}
	public String getLogar() {
		return logar;
	}
	public void setLogar(String logar) {
		this.logar = logar;
	}
	public String getDeslogar() {
		return deslogar;
	}
	public void setDeslogar(String deslogar) {
		this.deslogar = deslogar;
	}
	public String getExcluiconta() {
		return excluiconta;
	}
	public void setExcluiconta(String excluiconta) {
		this.excluiconta = excluiconta;
	}

}
